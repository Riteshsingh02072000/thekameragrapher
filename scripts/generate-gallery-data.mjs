import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, '..');
const portfolioRoot = path.join(projectRoot, 'public', 'portfolio');
const outputFile = path.join(projectRoot, 'src', 'data', 'generatedGalleryData.js');
const configFile = path.join(projectRoot, 'src', 'data', 'galleryConfig.js');

const IMAGE_EXTENSIONS = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif']);

function asset(relativePath) {
  return encodeURI(`/portfolio/${relativePath}`);
}

function toId(slug, index) {
  return `${slug}-${index + 1}`;
}

function toFallbackTitle(title, index) {
  return `${title} Frame ${String(index + 1).padStart(2, '0')}`;
}

function toAlt(title, index) {
  return `${title} photo ${index + 1}`;
}

function sortFiles(files) {
  return files.sort((left, right) =>
    left.localeCompare(right, undefined, {
      numeric: true,
      sensitivity: 'base',
    }),
  );
}

async function listImages(directoryPath) {
  const entries = await fs.readdir(directoryPath, { withFileTypes: true });
  return sortFiles(
    entries
      .filter((entry) => entry.isFile())
      .map((entry) => entry.name)
      .filter((fileName) => IMAGE_EXTENSIONS.has(path.extname(fileName).toLowerCase())),
  );
}

function serialize(value) {
  return JSON.stringify(value, null, 2);
}

async function generate() {
  const { galleryConfig } = await import(pathToFileURL(configFile).href);
  const categories = [];
  const counts = [];

  for (const [slug, categoryConfig] of Object.entries(galleryConfig.categories)) {
    const categoryPath = path.join(portfolioRoot, slug);
    const files = await listImages(categoryPath);

    if (files.length === 0) {
      throw new Error(`No images found for category "${slug}" in ${categoryPath}`);
    }

    const featuredByFile = new Map(
      (categoryConfig.featuredPhotos ?? []).map((photo, index) => [photo.file, { ...photo, featuredIndex: index }]),
    );

    const orderedFiles = [
      ...featuredByFile.keys(),
      ...files.filter((file) => !featuredByFile.has(file)),
    ].filter((file, index, array) => array.indexOf(file) === index && files.includes(file));

    const photos = orderedFiles.map((fileName, index) => {
      const featuredPhoto = featuredByFile.get(fileName);
      const src = asset(`${slug}/${fileName}`);

      return {
        id: toId(slug, index),
        src,
        alt: toAlt(categoryConfig.title, index),
        title: featuredPhoto?.title ?? toFallbackTitle(categoryConfig.title, index),
        meta: featuredPhoto?.meta ?? categoryConfig.defaultMeta,
      };
    });

    categories.push({
      slug,
      label: categoryConfig.label,
      title: categoryConfig.title,
      description: categoryConfig.description,
      longDescription: categoryConfig.longDescription,
      coverImage: {
        src: asset(`${slug}/${orderedFiles[0]}`),
        alt: `${categoryConfig.title} photography cover`,
      },
      photos,
    });

    counts.push(`${slug}: ${photos.length}`);
  }

  const fileContents = `/* eslint-disable */
// This file is auto-generated by scripts/generate-gallery-data.mjs.
// Run "npm run generate:gallery" after adding or removing files in public/portfolio.

export const heroImage = ${serialize(asset(galleryConfig.heroImage))};

export const categories = ${serialize(categories)};

export const categoriesBySlug = Object.fromEntries(categories.map((category) => [category.slug, category]));

export const navigationItems = categories.map(({ slug, title }) => ({
  slug,
  label: title,
}));
`;

  await fs.writeFile(outputFile, fileContents);
  console.log(`Generated ${path.relative(projectRoot, outputFile)}`);
  console.log(`Image counts -> ${counts.join(', ')}`);
}

generate().catch((error) => {
  console.error(error.message);
  process.exitCode = 1;
});
